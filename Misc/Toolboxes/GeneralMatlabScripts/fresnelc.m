function FC=fresnelc(T);
%--------------------------------------------------------
% fresnelc function     return: cos(0.5*pi*T^2)
% Input  : - Parameter.
% Output : - Fresnel cosine function.
% Tested : Matlab 5.3
%     By : Eran O. Ofek       April 2000
%    URL : http://wise-obs.tau.ac.il/~eran/matlab.html
%--------------------------------------------------------
FC = cos(0.5.*pi.*T.^2);
